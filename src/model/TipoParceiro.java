package model;


public class TipoParceiro {

	private int idTipoParceiro;
	private String tipoParceiro;
	
	
	/**
	 * @return the idTipoParceiro
	 */
	public int getIdTipoParceiro() {
		return idTipoParceiro;
	}
	/**
	 * @param idTipoParceiro the idTipoParceiro to set
	 */
	public void setIdTipoParceiro(int idTipoParceiro) {
		this.idTipoParceiro = idTipoParceiro;
	}
	
	
	/**
	 * @return the tipoParceiro
	 */
	public String getTipoParceiro() {
		return tipoParceiro;
	}
	/**
	 * @param tipoParceiro the tipoParceiro to set
	 */
	public void setTipoParceiro(String tipoParceiro) {
		this.tipoParceiro = tipoParceiro;
	}
	
	
}
